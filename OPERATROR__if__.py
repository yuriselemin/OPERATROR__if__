# Условный оператор if в Python используется для выполнения определенных действий, если условие истинно.


# Предположим, у нас есть переменная x со значением 5
x = 5

# Если x больше 0, то выполняем блок кода после оператора if
if x > 0:
    print("Значение переменной x положительное")

# Если x равно 0, то выполняем блок кода после оператора elif
elif x == 0:
    print("Значение переменной x равно нулю")

# Если x меньше 0, то выполняем блок кода после оператора else
else:
    print("Значение переменной x отрицательное")



# Проверка условия перед выполнением определённого действия.

x = int(input("Введите число: "))

if x % 2 == 0:
    print("Число", x, "является чётным.")
else:
    print("Число", x, "является нечётным.")




# Выбор между двумя действиями в зависимости от результата проверки условия.

fruit = input("Что вы хотите съесть? ")

if fruit == "яблоко":
    print("Вы выбрали яблоко.")
elif fruit == "банан":
    print("Вы выбрали банан.")
else:
    print("Я не знаю, что вы выбрали.")




# Использование вложенных операторов if для сложных проверок.

temperature = float(input("Какая температура сейчас? "))

if temperature < 0:
    print("Холодно!")
    if temperature <= -20:
        print("Очень холодно!")
    else:
        print("Просто холодно.")
elif temperature > 0:
    print("Тепло!")
else:
    print("Комфортная температура!")




# Использование оператора if для управления логикой программы.

count = 0
max_count = 3

while count < max_count:
    guess = int(input("Попробуйте угадать число: "))

    if guess == 5:
        print("Вы угадали!")
        break
    else:
        print("Неправильно, попробуйте ещё раз.")
        count += 1




